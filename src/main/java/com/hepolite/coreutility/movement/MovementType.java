package com.hepolite.coreutility.movement;

public enum MovementType
{
	/** In water, not moving */
	FLOATING,
	/** Flying through the air, moving */
	FLYING,
	/** Gliding through the air, moving */
	GLIDING,
	/** Flying, hovering in one spot */
	HOVERING,
	/** Running, moving on ground */
	RUNNING,
	/** Sneaking, moving on ground */
	SNEAKING,
	/** On ground, standing still */
	STANDING,
	/** In water, moving or standing still */
	SWIMMING,
	/** On ground, moving */
	WALKING,
}
